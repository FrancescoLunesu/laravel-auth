        // mi vado a prendere i dati che mi vengono restituiti sotto forma di array

        $request->validate(
            [
                'title'=>'required|min:5|max:100',
                'body'=>'required|min:5|max:500'
            ]
        );
        $data = $request->all();
        $data['user_id'] = Auth::id();
        $data['slug'] = Str::slug($data['title'], '-');
        $newPost = new Post();
        $newPost->fill($data);
        $saved = $newPost->save();
